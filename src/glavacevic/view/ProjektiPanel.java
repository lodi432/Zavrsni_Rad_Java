    private void ucitajProjekte() {

        DefaultListModel<Projekt> m = new DefaultListModel<>();
        o.getEntiteti(txtUvjet.getText()).forEach((p) -> {
            m.addElement(p);
        });
        lstProjekt.setModel(m);
    }

    private void ocistiPolja() {

        for (Component c : pnlPodaci.getComponents()) {
            if (c instanceof JTextField) {
                ((JTextField) c).setText("");
            }
        }
    }

    private boolean popuniSvojstva() {
        projekt.setNaziv(txtProjekt.getText());
        
        if(projekt.getDirektorij()==null || projekt.getDirektorij().isEmpty()){
//            JOptionPane.showMessageDialog(getRootPane(), "majketi di ces bez slike");
//            return false;
 projekt.setDirektorij(txtDirektorij.getText());
        }
        else { 
            projekt.setDirektorij(txtDirektorij.getText());
            
        }
       
        
        try {
            projekt.setTrajanje(Integer.parseInt(txtTrajanje.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(getRootPane(), "Trajanje obavezno cijeli broj");
            return false;
        }

        try {
            projekt.setCijena(new BigDecimal(df.parse(txtCijena.getText()).doubleValue()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(getRootPane(), "Cijena mora biti broj");
            return false;
        }

        return true;
    }

