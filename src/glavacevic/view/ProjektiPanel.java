    private void btn_slika1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_slika1ActionPerformed
        String filename = null;

        JFileChooser chooser = new JFileChooser();

        FileNameExtensionFilter filter = new FileNameExtensionFilter(
            "JPG, GIF, and PNG Images", "jpg", "gif", "png");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(null);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            System.out.println("You chose to open this file: "
                + file.getName());
            BufferedImage image;
            try {
                image = ImageIO.read(file);

                ImageIO.write(image, "jpg", new File(putanja + file.getName()));
            } catch (IOException ex) {
                Logger.getLogger(SpremiSliku.class.getName()).log(Level.SEVERE, null, ex);
            }

            //       chooser.showOpenDialog(null);
            //       File f = chooser.getSelectedFile();
            filename = file.getAbsolutePath();
            ImageIcon imageIcon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(lbl_img1.getWidth(), lbl_img1.getHeight(), Image.SCALE_SMOOTH));
            lbl_img1.setIcon(imageIcon);
            //            lbl_dirOdabrane.setText(filename);
            txtDirektorij.setText(file.getName());

        }
    }//GEN-LAST:event_btn_slika1ActionPerformed

