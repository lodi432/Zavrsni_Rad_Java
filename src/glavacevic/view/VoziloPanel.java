     private void ucitaj() {
        DefaultListModel<VoziloD> m = new DefaultListModel<>();
        List<VoziloD> lista = obrada.getEntiteti(txtUvjet.getText());

        //sortirati po prezimenu
        Collections.sort(lista, new Comparator<VoziloD>() {
            public int compare(VoziloD o1, VoziloD o2) {
                return o1.getProizvodacdobavljac().compareTo(o2.getProizvodacdobavljac());
            }
        });

        lista.forEach((s) -> {
            m.addElement(s);
        });
        lstEntiteti.setModel(m);
    }

