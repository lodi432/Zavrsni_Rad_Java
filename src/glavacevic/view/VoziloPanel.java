    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if (evt.getValueIsAdjusting()) {
            return;
        }

        entitet = lstEntiteti.getSelectedValue();
        if (entitet == null) {
            return;
        }
        ocistiPolja();
        
        txtIme.setText(entitet.getNazivdjela());
        txtDobavljac.setText(entitet.getProizvodacdobavljac());
        txtCijena.setText(df.format(entitet.getCijena()));
        txtModel.setText(entitet.getModel());
        
    }//GEN-LAST:event_lstEntitetiValueChanged

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
 
        lstEntiteti.getSelectedValuesList().forEach((e)->{
            try {
                obrada.obrisi(e);
                popuniTablicu();
            } catch (Exception ex) {
                HibernateUtil.getSession().clear();
                JOptionPane.showMessageDialog(getRootPane(), "Predavac " + e +
                    " ne mogu obrisati jer vodi neku grupa");
            }

        });

        ucitaj();
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnDodajNoviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajNoviActionPerformed

        entitet = new VoziloD();

        if (!popuniSvojstva()) {
            return;
        }

        ocistiBoje();

        try {

            VoziloD novi = obrada.dodaj(entitet);

            ucitaj();

            for(int i=0;i<lstEntiteti.getModel().getSize();i++){
                if(lstEntiteti.getModel().getElementAt(i).getSifra()
                    ==novi.getSifra()){
                    lstEntiteti.setSelectedIndex(i);
                    lstEntiteti.ensureIndexIsVisible(lstEntiteti.getSelectedIndex());
                    break;
                }
            }
                popuniTablicu();
        } catch (EdunovaException ex) {
            switch (ex.getPoruka()){
                case "Ime obavezno":
                txtIme.setBackground(Color.RED);
                break;
            }
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnDodajNoviActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        entitet = lstEntiteti.getSelectedValue();
        if (entitet == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberi predavača");
            return;
        }

        ocistiBoje();

        if (!popuniSvojstva()) {
            return;
        }

        try {
            obrada.promjena(entitet);
            ucitaj();
            popuniTablicu();
        } catch (EdunovaException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnPromjenaActionPerformed

