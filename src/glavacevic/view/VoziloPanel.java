  private boolean popuniSvojstva() {
        
        entitet.setNazivdjela(txtIme.getText());
        entitet.setProizvodacdobavljac(txtDobavljac.getText());
        
         try {
        entitet.setCijena(new BigDecimal(df.parse(txtCijena.getText()).doubleValue()));
         } catch (Exception e) {
            JOptionPane.showMessageDialog(getRootPane(), "Cijena mora biti broj");
            return false;
        }
        entitet.setModel(txtModel.getText()); 
        return true;
    }
  
  
